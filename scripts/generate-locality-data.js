/**
 * Generates LocalityData.cs from country-region-data package
 *
 * Usage: node scripts/generate-locality-data.js
 *
 * This script fetches country/region data from the country-region-data npm package
 * and generates a C# static class with all the subdivision data.
 */

const https = require('https');
const fs = require('fs');
const path = require('path');

const DATA_URL = 'https://raw.githubusercontent.com/country-regions/country-region-data/master/data.json';
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'Merchello.Core', 'Locality', 'Data', 'LocalityData.cs');

// Countries that should have subdivisions (matching Shopify's list)
const COUNTRIES_WITH_SUBDIVISIONS = new Set([
    'AR', // Argentina
    'AU', // Australia
    'BR', // Brazil
    'CA', // Canada
    'CN', // China
    'CO', // Colombia
    'EG', // Egypt
    'GT', // Guatemala
    'HK', // Hong Kong
    'IN', // India
    'ID', // Indonesia
    'IE', // Ireland
    'IT', // Italy
    'JP', // Japan
    'MY', // Malaysia
    'MX', // Mexico
    'NZ', // New Zealand
    'NG', // Nigeria
    'PA', // Panama
    'PE', // Peru
    'PT', // Portugal
    'RO', // Romania
    'RU', // Russia
    'ZA', // South Africa
    'KR', // South Korea
    'ES', // Spain
    'TH', // Thailand
    'AE', // UAE
    'GB', // United Kingdom
    'US', // United States
]);

// Override UK subdivisions to match Shopify (4 nations + British Forces)
const UK_OVERRIDE = {
    'ENG': 'England',
    'NIR': 'Northern Ireland',
    'SCT': 'Scotland',
    'WLS': 'Wales',
    'BFP': 'British Forces'
};

// US territories and military postal codes to add
const US_ADDITIONS = {
    'AA': 'Armed Forces Americas',
    'AE': 'Armed Forces Europe',
    'AP': 'Armed Forces Pacific',
    'AS': 'American Samoa',
    'GU': 'Guam',
    'MP': 'Northern Mariana Islands',
    'PR': 'Puerto Rico',
    'VI': 'Virgin Islands'
};

// Crown Dependencies (separate countries)
const ADDITIONAL_COUNTRIES = {
    'GG': 'Guernsey',
    'IM': 'Isle of Man',
    'JE': 'Jersey'
};

function fetchData(url) {
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            let data = '';
            res.on('data', chunk => data += chunk);
            res.on('end', () => resolve(JSON.parse(data)));
            res.on('error', reject);
        }).on('error', reject);
    });
}

function escapeString(str) {
    return str.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function generateCSharp(data) {
    const subdivisions = {};

    for (const country of data) {
        const countryCode = country.countryShortCode;

        // Skip if not in our list of countries with subdivisions
        if (!COUNTRIES_WITH_SUBDIVISIONS.has(countryCode)) {
            continue;
        }

        // Use override for UK
        if (countryCode === 'GB') {
            subdivisions[countryCode] = UK_OVERRIDE;
            continue;
        }

        // Process regions
        if (country.regions && country.regions.length > 0) {
            const regions = {};
            for (const region of country.regions) {
                if (region.shortCode) {
                    // Remove country prefix if present (e.g., "US-CA" -> "CA")
                    let code = region.shortCode;
                    if (code.includes('-')) {
                        code = code.split('-').pop();
                    }
                    regions[code] = region.name;
                }
            }

            // Add US territories
            if (countryCode === 'US') {
                Object.assign(regions, US_ADDITIONS);
            }

            if (Object.keys(regions).length > 0) {
                subdivisions[countryCode] = regions;
            }
        }
    }

    // Generate C# code
    let code = `// <auto-generated>
// This file was generated by scripts/generate-locality-data.js
// Do not modify manually. To update, run: node scripts/generate-locality-data.js
// Source: https://github.com/country-regions/country-region-data
// Generated: ${new Date().toISOString()}
// </auto-generated>

using System;
using System.Collections.Generic;

#nullable enable

namespace Merchello.Core.Locality.Data;

/// <summary>
/// Static locality data for countries and their subdivisions (states, provinces, regions).
/// Codes follow ISO 3166-2 suffix format (e.g., "ENG" not "GB-ENG").
/// </summary>
public static class LocalityData
{
    /// <summary>
    /// Crown Dependencies and special territories not included in .NET CultureInfo.
    /// These should be added to the country list as separate countries.
    /// </summary>
    public static readonly IReadOnlyDictionary<string, string> AdditionalCountries =
        new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)
        {
`;

    // Add additional countries
    const additionalEntries = Object.entries(ADDITIONAL_COUNTRIES)
        .sort(([a], [b]) => a.localeCompare(b));
    for (let i = 0; i < additionalEntries.length; i++) {
        const [countryCode, name] = additionalEntries[i];
        const comma = i < additionalEntries.length - 1 ? ',' : '';
        code += `            ["${countryCode}"] = "${escapeString(name)}"${comma}\n`;
    }

    code += `        };

    /// <summary>
    /// Province/state/region data by country code.
    /// Codes are ISO 3166-2 suffix only (e.g., "CA" not "US-CA").
    /// </summary>
    public static readonly IReadOnlyDictionary<string, IReadOnlyDictionary<string, string>> Subdivisions =
        new Dictionary<string, IReadOnlyDictionary<string, string>>(StringComparer.OrdinalIgnoreCase)
        {
`;

    // Sort countries alphabetically
    const sortedCountries = Object.entries(subdivisions)
        .sort(([a], [b]) => a.localeCompare(b));

    for (let i = 0; i < sortedCountries.length; i++) {
        const [countryCode, regions] = sortedCountries[i];
        const isLast = i === sortedCountries.length - 1;

        code += `            ["${countryCode}"] = new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)\n`;
        code += `            {\n`;

        // Sort regions alphabetically by code
        const sortedRegions = Object.entries(regions)
            .sort(([a], [b]) => a.localeCompare(b));

        for (let j = 0; j < sortedRegions.length; j++) {
            const [regionCode, regionName] = sortedRegions[j];
            const regionComma = j < sortedRegions.length - 1 ? ',' : '';
            code += `                ["${regionCode}"] = "${escapeString(regionName)}"${regionComma}\n`;
        }

        code += `            }${isLast ? '' : ','}\n`;
    }

    code += `        };

    /// <summary>
    /// Gets subdivisions for a country, or an empty dictionary if none are defined.
    /// </summary>
    /// <param name="countryCode">ISO 3166-1 alpha-2 country code (e.g., "US", "GB")</param>
    /// <returns>Dictionary of region codes to region names</returns>
    public static IReadOnlyDictionary<string, string> GetSubdivisions(string countryCode)
    {
        if (string.IsNullOrWhiteSpace(countryCode))
            return new Dictionary<string, string>();

        return Subdivisions.TryGetValue(countryCode.ToUpperInvariant(), out var subs)
            ? subs
            : new Dictionary<string, string>();
    }

    /// <summary>
    /// Formats a country and region code as ISO 3166-2 format.
    /// </summary>
    /// <param name="countryCode">ISO 3166-1 alpha-2 country code (e.g., "US")</param>
    /// <param name="regionCode">Region code suffix (e.g., "CA")</param>
    /// <returns>Full ISO 3166-2 code (e.g., "US-CA") or just country code if no region</returns>
    public static string FormatIso3166_2(string countryCode, string? regionCode)
    {
        if (string.IsNullOrWhiteSpace(countryCode))
            return string.Empty;

        var cc = countryCode.ToUpperInvariant();

        if (string.IsNullOrWhiteSpace(regionCode))
            return cc;

        return $"{cc}-{regionCode.ToUpperInvariant()}";
    }
}
`;

    return code;
}

async function main() {
    console.log('Fetching country-region-data...');
    const data = await fetchData(DATA_URL);
    console.log(`Fetched ${data.length} countries`);

    console.log('Generating C# code...');
    const csharpCode = generateCSharp(data);

    // Ensure directory exists
    const dir = path.dirname(OUTPUT_PATH);
    if (!fs.existsSync(dir)) {
        fs.mkdirSync(dir, { recursive: true });
    }

    fs.writeFileSync(OUTPUT_PATH, csharpCode, 'utf8');
    console.log(`Generated: ${OUTPUT_PATH}`);

    // Count subdivisions
    const subdivisionCount = csharpCode.match(/\["[A-Z]{2,3}"\] = "/g)?.length || 0;
    console.log(`Total subdivision entries: ${subdivisionCount}`);
}

main().catch(err => {
    console.error('Error:', err);
    process.exit(1);
});
